**BÖLÜM 3: BAĞLAÇLARIN YAZIMI**

**3.1. Bağlaç Olan "da, de"**

1.  **Kural:** Bağlaç olan *da/de* her zaman ayrı yazılır. Cümleden çıkarıldığında cümlenin anlamı bozulmaz, sadece daralır.
    *   Örnek: *Kızı da geldi gelini de.* (Cümleden çıkaralım: *Kızı geldi gelini.* Anlam bozulmadı.)
    *   Örnek: *Durumu oğluna da bildirdi.*
2.  **Ünlü Uyumu:** Kendinden önceki kelimenin son ünlüsüne göre *da* veya *de* şeklinde yazılır.
3.  **"ta, te" Olmaz:** Ayrı yazılan *da/de* bağlacı kesinlikle sertleşerek *ta/te* biçiminde yazılmaz.
    *   Yanlış: *Gidip te gelmemek var.*
    *   Doğru: *Gidip de gelmemek var.*
4.  **Kesme İşareti Kullanılmaz:** Özel isimlerden sonra gelse bile kesme işareti ile ayrılmaz.
    *   Yanlış: *Ahmet'de geldi.*
    *   Doğru: *Ahmet de geldi.*
5.  **"ya da"**: *ya* sözcüğüyle birlikte kullanılan *da* her zaman ayrı yazılır.

**3.2. Bulunma Durumu Eki "-da, -de, -ta, -te"**

1.  **Kural:** Ek olan *-da/-de* her zaman kendinden önceki kelimeye bitişik yazılır. Cümleden çıkarıldığında cümlenin anlamı bozulur.
    *   Örnek: *Kitaplarım evde kalmış.* (Cümleden çıkaralım: *Kitaplarım ev kalmış.* Anlam bozuldu.)
2.  **Ünsüz Benzeşmesi (Sertleşme):** Eklendiği kelimenin sonu sert ünsüzle (f, s, t, k, ç, ş, h, p) bitiyorsa, *da/de* eki sertleşerek *ta/te* olur.
    *   Örnek: *Bu sokakta kimse yok.* (sokak + da -> sokakta)
    *   Örnek: *Saat üçte buluşalım.* (üç + de -> üçte)
3.  **Kesme İşareti:** Özel isimlere geldiğinde kesme işareti ile ayrılır.
    *   Örnek: *Ahmet'te yeni bir kitap var.*

**3.3. Bağlaç Olan "ki"**

1.  **Kural:** Bağlaç olan *ki* her zaman ayrı yazılır.
    *   Örnek: *Bilmem ki...*, *Demek ki...*, *Geçmiş zaman olur ki...*
2.  **Kalıplaşmış Olanlar (İstisnalar):** **S**anki, **İ**llaki, **M**ademki, **B**elki, **O**ysaki, **H**âlbuki, **Ç**ünkü, **M**eğerki kelimelerindeki *ki* bağlacı kalıplaşmış olduğu için bitişik yazılır. (Akılda tutmak için **SİMBOHÇAM** olarak kodlanabilir.)

